package dev.rentalcore.entity.enums;

/**
 * Универсальный статус объекта аренды.
 *
 * <p>Используется в {@code AbstractRentalProduct} для отслеживания
 * текущего состояния объекта в системе.</p>
 *
 * <ul>
 *   <li>{@link #FREE} — объект доступен для аренды</li>
 *   <li>{@link #USED} — объект находится в аренде</li>
 *   <li>{@link #PENDING} — объект ожидает проверки/верификации</li>
 *   <li>{@link #DELETED} — объект удалён (мягкое удаление)</li>
 * </ul>
 */
public enum ProductStatus {

    /** Объект свободен и доступен для аренды. */
    FREE,

    /** Объект в данный момент арендован. */
    USED,

    /** Объект ожидает верификации модератором. */
    PENDING,

    /** Объект удалён (мягкое удаление, запись сохраняется в БД). */
    DELETED
}
