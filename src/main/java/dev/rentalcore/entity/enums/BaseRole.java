package dev.rentalcore.entity.enums;

/**
 * Базовые роли пользователя в системе аренды.
 *
 * <p>Клиент может расширить набор ролей, добавив собственный enum
 * и переопределив поле role в дочернем классе {@code AbstractUser}.</p>
 *
 * <ul>
 *   <li>{@link #USER} — обычный пользователь (арендатор / арендодатель)</li>
 *   <li>{@link #ADMIN} — администратор с расширенными правами</li>
 * </ul>
 */
public enum BaseRole {

    /** Обычный пользователь системы. */
    USER,

    /** Администратор системы. */
    ADMIN
}
